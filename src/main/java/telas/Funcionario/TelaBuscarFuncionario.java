    private void buttonExcluirFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirFuncActionPerformed
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza de que deseja excluir funcionário?", "CONFIRMAÇÃO DE EXCLUSÃO", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if(confirma == JOptionPane.YES_OPTION){
            if(tabelaFuncionario.getSelectedRow() != -1){
                Funcionario funcionario = new Funcionario();
                FuncionarioDAO dao = new FuncionarioDAO();

                funcionario.setId((int) tabelaFuncionario.getValueAt(tabelaFuncionario.getSelectedRow(), 0));
                dao.deletar(funcionario);
            }else{
                JOptionPane.showMessageDialog(null, "Selecione um funcionario para excluir!");
            }
        }
    }//GEN-LAST:event_buttonExcluirFuncActionPerformed

    private void txtArgFuncKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtArgFuncKeyReleased
        DefaultTableModel model = (DefaultTableModel) tabelaFuncionario.getModel();
        model.setNumRows(0);
        
        String tipo = "";
        String escolha = cBoxTipoFunc.getSelectedItem().toString();
        
        if(escolha.equals("Nome")){
            tipo = " "+"nome";
        }
        if(escolha.equals("Código")){
            tipo = " "+"id";
        }
        String arg = txtArgFunc.getText();
        
        try{
            ResultSet rs = FuncionarioDAO.carregaTabela(tipo, arg);
            DefaultTableModel dtm = (DefaultTableModel) tabelaFuncionario.getModel();
           
            while(rs.next()){
                String Coluna0=rs.getString("id");
                String Coluna1=rs.getString("nome");
                String Coluna2=rs.getString("CPF");
                String Coluna3=rs.getString("telefone");
                String Coluna4=rs.getString("tipo_func");
                String Coluna5=rs.getString("especialidade");
                
                dtm.addRow(new String[]{Coluna0,Coluna1,Coluna2,Coluna3,Coluna4,Coluna5});
            }           
        }
        catch(SQLException erro){
            JOptionPane.showMessageDialog(this,"ERRO!"+erro, "Preencher Item", 2);
        }
        tabelaFuncionario.setAutoCreateRowSorter(true);
        
    }//GEN-LAST:event_txtArgFuncKeyReleased

