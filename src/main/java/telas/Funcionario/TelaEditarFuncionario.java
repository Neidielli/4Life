    private void SalvarEdtFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarEdtFuncActionPerformed
        try{
            Funcionario func = new Funcionario();

            func.setNome(txtNomeFunc.getText());
            func.setCPF(txtCPFfunc.getText());
            func.setTipo_func(String.valueOf(cBoxTipoFunc.getSelectedItem()));
            func.setTelefone(campoTelefFunc.getText());
            func.setEmail(txtEmailFunc.getText());
            func.setEspecialidade(especialidadeEdtFunc.getText());
            func.setId(Integer.parseInt(txtCodFunc.getText()));
            

            FuncionarioDAO fdao = new FuncionarioDAO();
            fdao.editar(func);

            JOptionPane.showMessageDialog(null, "Funcionário atualizado com sucesso!");

            // apaga os dados preenchidos nos campos de texto
            txtCodFunc.setText("");
            txtNomeFunc.setText("");
            txtCPFfunc.setText("");
            cBoxTipoFunc.setSelectedItem("selecione um tipo");
            campoTelefFunc.setText("");
            txtEmailFunc.setText("");
            especialidadeEdtFunc.setText("");
        }
        catch(Exception erro){
            JOptionPane.showMessageDialog(null, "Erro ao alterar dados!" + erro);
        }
    }//GEN-LAST:event_SalvarEdtFuncActionPerformed

