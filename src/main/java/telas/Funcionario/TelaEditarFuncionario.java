    private void buttonPesqFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonPesqFuncActionPerformed
        Funcionario funcionario = new Funcionario();
        
        if(txtCodFunc.getText().equals("")){  //chama a tela de Busca Paciente quando o campo estiver vazio.
            TelaBuscarFuncionario telaBusca = new TelaBuscarFuncionario();
            telaBusca.setVisible(true);
        }
        else{
            funcionario.setId(Integer.parseInt(txtCodFunc.getText()));
            FuncionarioDAO pdao = new FuncionarioDAO();
            
            try{
                if(pdao.pesquisaFunc(funcionario)){
                    if(funcionario != null){
                        txtNomeFunc.setText(funcionario.getNome().trim());                  
                        txtCPFfunc.setText(funcionario.getCPF().trim());
                        cBoxTipoFunc.setSelectedItem(funcionario.getTipo_func());
                        campoTelefFunc.setText(funcionario.getTelefone().trim());
                        txtEmailFunc.setText(funcionario.getEmail().trim());
                        especialidadeEdtFunc.setText(funcionario.getEspecialidade().trim());
                    }
                }
            }
            catch(Exception exc){
                JOptionPane.showMessageDialog(null, "Erro ao pesquisar funcionário!" +exc);
            }
        }  
    }//GEN-LAST:event_buttonPesqFuncActionPerformed

