    private void buttonEditaUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditaUserActionPerformed
        Usuario user = new Usuario();
        
        TelaEditarUsuario telaEditarUsuario = new TelaEditarUsuario();
        telaEditarUsuario.setVisible(true); // abre a tela de edição novamente
        
        int linha = tabelaUsuario.getSelectedRow();  //pega o código da linha selecionada e passa para a tela de edição.
        
        user.setId(Integer.parseInt(tabelaUsuario.getValueAt(linha,0).toString())); 
        
        TelaEditarUsuario.recebeId(user); 
        this.dispose();
    }//GEN-LAST:event_buttonEditaUserActionPerformed

    private void buttonExcluiUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluiUserActionPerformed
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza de que deseja excluir usuário?", "CONFIRMAÇÃO DE EXCLUSÃO", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if(confirma == JOptionPane.YES_OPTION){
            if(tabelaUsuario.getSelectedRow() != -1){
                Usuario usuario = new Usuario();
                UsuarioDAO dao = new UsuarioDAO();

                usuario.setId((int) tabelaUsuario.getValueAt(tabelaUsuario.getSelectedRow(), 0));
                dao.deletar(usuario);
            }else{
                JOptionPane.showMessageDialog(null, "Selecione um usuário para excluir!");
            }
        }
    }//GEN-LAST:event_buttonExcluiUserActionPerformed

    private void txtArgKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtArgKeyReleased
        DefaultTableModel model = (DefaultTableModel) tabelaUsuario.getModel();
        model.setNumRows(0);
        
        String tipo = "";
        String escolha = cBoxTipo.getSelectedItem().toString();
        
        if(escolha.equals("Nome")){
            tipo = " "+"nome";
        }
        if(escolha.equals("Código")){
            tipo = " "+"id";
        }
        String arg = txtArg.getText();
        
        try{
            ResultSet rs = UsuarioDAO.carregaTabela(tipo, arg);
            DefaultTableModel dtm = (DefaultTableModel) tabelaUsuario.getModel();
            
            //pega os dados da linha no banco de dados.
            while(rs.next()){
                String Coluna0=rs.getString("id");
                String Coluna1=rs.getString("nome");
                String Coluna2=rs.getString("cpf");
                String Coluna3=rs.getString("email");
                String Coluna4=rs.getString("telefone");
                
                dtm.addRow(new String[]{Coluna0,Coluna1,Coluna2,Coluna3,Coluna4});
            }           
        }
        catch(SQLException erro){
            JOptionPane.showMessageDialog(this,"ERRO!"+erro, "Preencher Item", 2);
        }
        tabelaUsuario.setAutoCreateRowSorter(true);
    }//GEN-LAST:event_txtArgKeyReleased

